void print_hex(uint8_t *arr1, uint8_t *arr2, size_t size)
{
    for (size_t i = 0; i < size; ++i) {
	if (i % 8 == 0) printf("\n");
	printf("0x%02x ", arr1[i]);
    }
    printf("\n");
    
    for (size_t i = 0; i < size; ++i) {
	if (i % 8 == 0) printf("\n");
	printf("0x%02x ", arr2[i]);
    }
    printf("\n");
}

bool check(uint8_t *output, uint8_t *expected, size_t size, const char *name)
{
    for (size_t i = 0; i < size; ++i) {
        if (expected[i] != output[i]) {
	    printf("[FAIL] %s\n", name);
	    print_hex(output, expected, size);
	    return false;
	}
    }
    printf("[PASS] %s\n", name);
    return true;
}

